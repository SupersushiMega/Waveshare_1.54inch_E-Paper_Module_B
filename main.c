void Init()
{	
	//Reset Display
	//==================================================================
	PORTD &= ~(1<<PD2);
	Wait();
	PORTD |= (1<<PD2);
	Wait();
	//==================================================================
	
	//Power settings
	//==================================================================
	SPI_Com(0x01);
	SPI_Data(0x07);
	SPI_Data(0x00);
	SPI_Data(0x08);
	SPI_Data(0x00);
	//==================================================================
	
	//Booster Soft Start
	//==================================================================
	SPI_Com(0x06);
	SPI_Data(0x07);
	SPI_Data(0x07);
	SPI_Data(0x07);
	//==================================================================
	
	SPI_Com(0x04); //Power ON
	while(BUSY)
	{
		SPI_Com(0x71);	//Read data from 0x71
	}
	Wait4Idle();
	
	//Panel Setting
	//==================================================================
	SPI_Com(0x00);
	SPI_Data(0xCF);
	//==================================================================
	
	//Vcom and data interval setting
	//==================================================================
	SPI_Com(0x50);
	SPI_Data(0x17);
	//==================================================================
	
	//PLL control
	//==================================================================
	SPI_Com(0x30);
	SPI_Data(0x39);
	//==================================================================
	
	//Resolution settings
	//==================================================================
	SPI_Com(0x61);
	SPI_Data(DispWidth);
	SPI_Data(0x00);
	SPI_Data(DispHeight);
	//==================================================================
	
	//VCM_DC setting
	//==================================================================
	SPI_Com(0x82);
	SPI_Data(0x0E);
	//==================================================================
	
	initialised = 1;
	
	SetLutBw();
	SetLutY();
}


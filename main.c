int main(void)
{
	// Set MOSI, SCK as Output
    DDRB |= (1<<PB3) | (1<<PB5) | (1<<PB2) | (1<<PB1);
	DDRB &= ~(1<<PB4);//Input (Miso)
	PORTB |= (1<<PB1);
	PORTB &= ~(1<<PB2);
	//PORTD |= (1<<PD0);//epaper-BUSY; activate pullup
    // Enable SPI, Set as Master
    // Prescaler: Fosc/16, Enable Interrupts
    //The MOSI, SCK pins are as per ATMega8
    // Enable SPI as master, set clock rate fck/2
    //Init SPI		CLK/2
	//==================================================================
	SPCR = (1<<SPE) | (1<<MSTR);
	SPSR |= (1<<SPI2X);
	//==================================================================
 
    // Enable Global Interrupts
    sei();
	
	DDRD |= (1<<PD0) | (1<<PD1) | (1<<PD2);	//Pin definition CS DS Reset
	PORTD &= ~(1<<PD1);
	PORTD &= ~(1<<PD0);	//set CS to low
	PORTD |= (1<<PD2);	//set Reset to high
	
	DDRC &= ~(1<<PC1);	//Pin definition Input Button
	PORTC |= (1<<PC1);	//activate Pull up
	
	DDRC &= ~(1<<PD3);	//Pin definition Busy Input
	PORTD |= (1<<PD3);	//activate Pull up
	
	Init();
	
	drawString("Test\n Test Test", 0, 1, 10, 5);
	drawImage(&image, 80, 80, 0, 3, 4, 65);
	refresh();
	
	Wait4Idle();
	SPI_Com(0x02);
	while(1);
}//end of main

void drawString (char str[], uint8_t yellow, uint8_t inverted, uint8_t x, uint8_t y)
{
	char LetterMap[10][19] = {{0x00}};
	uint8_t counter = 0;
	uint8_t Xcount = 0;
	uint8_t Ycount = 0;
	//~ char stri[] = "TEST";
	for (Ycount = 0; Ycount < 10; Ycount++)
	{
		for (Xcount = 0; Xcount < 19; Xcount++)
		{
			if((counter < strlen(str)) && (Xcount >= x || Ycount > y) && (Ycount >= y))
			{
				if(toupper(str[counter]) != '\n')
				{
					LetterMap[Ycount][Xcount] = toupper(str[counter]);
					counter++;
				}
				else
				{
					while(Xcount < 19)
					{
						LetterMap[Ycount][Xcount] = ' ';
						Xcount++;
					}
					counter++;
				}
			}
			else
			{
				LetterMap[Ycount][Xcount] = ' ';
			}
		}
	}
	if (yellow == 0)
	{
		SPI_Com(0x10);
	}
	else
	{
		SPI_Com(0x13);
	}
	for (Ycount = 0; Ycount < 10; Ycount++)
	{
		for(counter = 0; counter < 15; counter++)
		{
			for (Xcount = 0; Xcount < 19; Xcount++)
			{
				if((inverted == 0 && yellow == 0) || (inverted != 0 && yellow != 0))
				{
					SPI_Data(pgm_read_byte(&Font[LetterMap[Ycount][Xcount]-32][counter]));
				}
				else
				{
					SPI_Data(~pgm_read_byte(&Font[LetterMap[Ycount][Xcount]-32][counter]));
				}
			}
		}
	}
}

void drawImage (uint8_t *image, uint8_t width, uint8_t height, uint8_t mainCol, uint8_t backCol, uint8_t X, uint8_t Y)
{
	uint16_t counter = 0;
	uint16_t x = 0;
	//~ uint8_t y = 0;
	
	if(mainCol == 0 || backCol == 0)
	{
		SPI_Com(0x10);
		for (counter = 0; counter < ((152*152)/8); counter++)
		{
			if(((((counter - X) * 8) % 152) < width) && (x < (height * (width / 8))) && ((counter % 19) >= X) && ((counter / 19) >= Y))
			{
				if(mainCol == 0 && backCol == 0)
				{
					Wait4Idle();
					SPI_Data(0x00);
					x++;
				}
				else if(mainCol == 0)
				{
					Wait4Idle();
					SPI_Data(pgm_read_byte(&image[x]));
					x++;
				}
				else if(backCol == 0)
				{
					Wait4Idle();
					SPI_Data(~pgm_read_byte(&image[x]));
					x++;
				}
				else
				{
					Wait4Idle();
					SPI_Data(0xff);
					x++;
				}
			}
			else
			{
				Wait4Idle();
				SPI_Data(0xff);
			}
		}
	}
	
	counter = 0;
	x = 0;
	
	Wait4Idle();
	
	if(mainCol == 3 || backCol == 3)
	{
		SPI_Com(0x13);
		for (counter = 0; counter < ((152*152)/8); counter++)
		{
			if(((((counter - X) * 8) % 152) < width) && (x < (height * (width / 8))) && ((counter % 19) >= X) && ((counter / 19) >= Y))
			{
				if(mainCol == 3 && backCol == 3)
				{
					Wait4Idle();
					SPI_Data(0xff);
					x++;
				}
				else if(mainCol == 3)
				{
					Wait4Idle();
					SPI_Data(~pgm_read_byte(&image[x]));
					x++;
				}
				else if(backCol == 3)
				{
					Wait4Idle();
					SPI_Data(pgm_read_byte(&image[x]));
					x++;
				}
				else
				{
					Wait4Idle();
					SPI_Data(0x00);
					x++;
				}
			}
			else
			{
				Wait4Idle();
				SPI_Data(0x00);
			}
		}
	}
}

void refresh()
{
	Wait4Idle();
	SPI_Com(0x12);
}





void drawString (char str[], uint8_t yellow, uint8_t inverted, uint8_t invertedSpace, uint8_t x, uint8_t y)
{
	if (initialised == 0)
	{
		Init();
	}
	char LetterMap[10][19] = {{0}};
	uint8_t counter = 0;
	uint8_t Xcount = 0;
	uint8_t Ycount = 0;
	//~ char stri[] = "TEST";
	for (Ycount = 0; Ycount < 10; Ycount++)
	{
		for (Xcount = 0; Xcount < 19; Xcount++)
		{
			if((counter < strlen(str)) && (Xcount >= x || Ycount > y) && (Ycount >= y))
			{
				if(str[counter] != '\n')
				{
					LetterMap[Ycount][Xcount] = str[counter];
				}
				else
				{
					while(Xcount < 19)
					{
						LetterMap[Ycount][Xcount] = ' ';
						Xcount++;
					}
				}
				counter++;
			}
			else
			{
				if (counter < strlen(str))
				{
					LetterMap[Ycount][Xcount] = 0;
				}
			}
		}
	}
	if (yellow == 0)
	{
		SPI_Com(0x10);
	}
	else
	{
		SPI_Com(0x13);
	}
	for (Ycount = 0; Ycount < 10; Ycount++)
	{
		for(counter = 0; counter < 15; counter++)
		{
			for (Xcount = 0; Xcount < 19; Xcount++)
			{
				if (LetterMap[Ycount][Xcount] != 0)
				{
					if(invertedSpace == yellow)
					{
						if(LetterMap[Ycount][Xcount] == ' ' && invertedSpace == 0)
						{
							SPI_Data(~pgm_read_byte(&Font[LetterMap[Ycount][Xcount]-32][counter]));
						}
						else
						{
							SPI_Data(pgm_read_byte(&Font[LetterMap[Ycount][Xcount]-32][counter]));
						}
					}
					else
					{
						if(LetterMap[Ycount][Xcount] == ' ' && invertedSpace == 0)
						{
							SPI_Data(pgm_read_byte(&Font[LetterMap[Ycount][Xcount]-32][counter]));
						}
						else
						{
							SPI_Data(~pgm_read_byte(&Font[LetterMap[Ycount][Xcount]-32][counter]));
						}
					}
				}
				else if (Xcount != 0)
				{
					if (invertedSpace == yellow)
					{
						SPI_Data(0xff);
					}
					else
					{
						SPI_Data(0x00);
					}
				}
				else
				{
					Xcount = 19;
					counter = 15;
					Ycount = 10;
				}
			}
		}
	}
}

void drawImage (uint8_t *image, uint8_t width, uint8_t height, uint8_t mainCol, uint8_t backCol, uint8_t X, uint8_t Y, uint8_t repeat)
{
	if (initialised == 0)
	{
		Init();
	}
	
	uint16_t counter = 0;
	uint16_t x = 0;
	uint8_t x2 = 0;
	
	if(mainCol == 0 || backCol == 0)
	{
		SPI_Com(0x10);
		for (counter = 0; counter < ((152*152)/8); counter++)
		{
			if(((((counter - X) * 8) % 152) < width) && (x < (height * (width / 8)) || repeat != 0) && ((counter % 19) >= X) && ((counter / 19) >= Y))
			{
				Wait4Idle();
				if(mainCol == 0 && backCol == 0)
				{
					SPI_Data(0x00);
				}
				else if(mainCol == 0)
				{
					SPI_Data(pgm_read_byte(&image[x % ((width / 8) * height)]));
				}
				else if(backCol == 0)
				{
					SPI_Data(~pgm_read_byte(&image[x % ((width / 8) * height)]));
				}
				else
				{
					SPI_Data(0xff);
				}
				x++;
				x2 = 0;
			}
			else
			{
				if (repeat == 0)
				{
					if(x <= (height * (width / 8)))
					{
						break;
					}
					else
					{
						Wait4Idle();
						SPI_Data(0xff);
					}
				}
				else
				{
					Wait4Idle();
					if(mainCol == 0 && backCol == 0)
					{
						SPI_Data(0x00);
					}
					else if(mainCol == 0)
					{
						SPI_Data(pgm_read_byte(&image[(x2 + ((width / 8) * ((counter / 19) % height))) % (x % ((width / 8) * height))]));
					}
					else if(backCol == 0)
					{
						SPI_Data(~pgm_read_byte(&image[(x2 + ((width / 8) * ((counter / 19) % height))) % (x % ((width / 8) * height))]));
					}
					else
					{
						SPI_Data(0xff);
					}
					x2++;
				}
			}
		}
	}
	
	counter = 0;
	x = 0;
	
	Wait4Idle();
	
	if(mainCol == 2 || backCol == 2)
	{
		SPI_Com(0x13);
		for (counter = 0; counter < ((152*152)/8); counter++)
		{
			if(((((counter - X) * 8) % 152) < width) && (x < (height * (width / 8)) || repeat != 0) && ((counter % 19) >= X) && ((counter / 19) >= Y))
			{
				if(mainCol == 2 && backCol == 2)
				{
					Wait4Idle();
					SPI_Data(0xff);
					x++;
				}
				else if(mainCol == 2)
				{
					Wait4Idle();
					SPI_Data(~pgm_read_byte(&image[x % ((width / 8) * height)]));
					x++;
				}
				else if(backCol == 2)
				{
					Wait4Idle();
					SPI_Data(pgm_read_byte(&image[x % ((width / 8) * height)]));
					x++;
				}
				else
				{
					Wait4Idle();
					SPI_Data(0x00);
					x++;
				}
				x2 = 0;
			}
			else
			{
				if (repeat == 0)
				{
					Wait4Idle();
					SPI_Data(0x00);
				}
				else
				{
					if(mainCol == 2 && backCol == 2)
					{
						Wait4Idle();
						SPI_Data(0xff);
						x2++;
					}
					else if(mainCol == 2)
					{
						Wait4Idle();
						SPI_Data(~pgm_read_byte(&image[(x2 + ((width / 8) * ((counter / 19) % height))) % (x % ((width / 8) * height))]));
						x2++;
					}
					else if(backCol == 2)
					{
						Wait4Idle();
						SPI_Data(pgm_read_byte(&image[(x2 + ((width / 8) * ((counter / 19) % height))) % (x % ((width / 8) * height))]));
						x2++;
					}
					else
					{
						if(x <= (height * (width / 8)))
						{
							break;
						}
						else
						{
							Wait4Idle();
							SPI_Data(0x00);
						}
					}
				}
			}
		}
	}
}


void SPI_Com (uint8_t data)
{/* Start transmission */
	//_delay_ms(10);
	PORTD &= ~(1<<PD0);
	PORTD &= ~(1<<PD1);
	SPDR = data;
	/* Wait for transmission complete */
	while(!(SPSR & (1<<SPIF)))
	;
}

void SPI_Data (uint8_t data)
{/* Start transmission */
	PORTD &= ~(1<<PD0);
	PORTD |= (1<<PD1);
	SPDR = data;
	/* Wait for transmission complete */
	while(!(SPSR & (1<<SPIF)))
	;
}


void defineWindow(uint8_t x, uint8_t y,  uint8_t width, uint8_t height)
{
	Wait4Idle();
	SPI_Com(0x91);
	SPI_Com(0x90);
	SPI_Data(x<<3);		//define x start
	SPI_Data(((x+(width/2))<<3) + 5);	//define x end
	//define y start
	//==================================================================
	SPI_Data(y>>8);	
	SPI_Data(y);
	//==================================================================
	
	//define y end
	//==================================================================
	SPI_Data((y+height)>>8);
	SPI_Data((y+height));
	//==================================================================
	
	SPI_Data(0x01);	//set gate scan mode
}




